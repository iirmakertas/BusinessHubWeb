// Global capture listener to detect manual language button clicks
window.userClickedLangBtn = false;
document.addEventListener('click', (e) => {
    if (e.target && (e.target.classList.contains('lang-btn') || e.target.closest('.lang-btn'))) {
        window.userClickedLangBtn = true;
        setTimeout(() => { window.userClickedLangBtn = false; }, 200);
    }
}, true);

function changeLanguage(lang, isInit = false) {
    localStorage.setItem('ab_lang', lang);
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = translations[lang][key];
            } else {
                const icon = el.querySelector('i');
                if (icon) {
                    el.innerHTML = translations[lang][key] + ' ' + icon.outerHTML;
                } else {
                    el.textContent = translations[lang][key];
                }
            }
        }
    });
    
    document.querySelectorAll('.lang-btn').forEach(btn => btn.classList.remove('active'));
    if(document.getElementById('btn-' + lang)) {
        document.getElementById('btn-' + lang).classList.add('active');
    }
    
    // Uygulama genelinde dilin değiştiğini bildir
    if (!isInit) {
        const isManual = window.userClickedLangBtn;
        window.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang, isManual } }));
    }
}

function toggleTheme() {
    const currentTheme = document.documentElement.getAttribute('data-theme') || 'light';
    const newTheme = currentTheme === 'light' ? 'dark' : 'light';
    document.documentElement.setAttribute('data-theme', newTheme);
    localStorage.setItem('ab_theme', newTheme);
    
    const icon = document.getElementById('theme-icon');
    if(icon) {
        icon.className = newTheme === 'light' ? 'fas fa-moon' : 'fas fa-sun';
    }
}

document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('ab_lang') || 'tr';
    changeLanguage(savedLang, true);
    
    const savedTheme = localStorage.getItem('ab_theme');
    if (savedTheme) {
        document.documentElement.setAttribute('data-theme', savedTheme);
        const icon = document.getElementById('theme-icon');
        if(icon) icon.className = savedTheme === 'light' ? 'fas fa-moon' : 'fas fa-sun';
    }
});
